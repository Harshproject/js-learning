// Data types in JavaScript
// 1. String
// 2. Number
// 3. Boolean
// 4. Null
// 5. Undefined
// 6. Symbol (ES6)
// 7. BigInt (ES2020)

// String
let name = 'Alice';
console.log(name); // Output: Alice

// Number
let age = 30;
console.log(age); // Output: 30

// Boolean
let isStudent = true;
console.log(isStudent); // Output: true

// Null
let emptyValue = null;
console.log(emptyValue); // Output: null        

// Undefined is not a value but null is a value which represents no value, 
// it is used to represent the absence of a value, it is an assignment value

